def _lesson_player_meta(video_url: str | None) -> dict:
    if not video_url:
        return {"mode": "video", "embed_url": None}

    parsed = urlparse(video_url)
    host = (parsed.netloc or "").lower()

    if "youtube.com" in host:
        video_id = parse_qs(parsed.query).get("v", [None])[0]
        if video_id:
            return {"mode": "iframe", "embed_url": f"https://www.youtube.com/embed/{video_id}"}
    if "youtu.be" in host:
        video_id = parsed.path.strip("/").split("/")[0] if parsed.path.strip("/") else None
        if video_id:
            return {"mode": "iframe", "embed_url": f"https://www.youtube.com/embed/{video_id}"}
    if "vimeo.com" in host:
        video_id = parsed.path.strip("/").split("/")[0] if parsed.path.strip("/") else None
        if video_id and video_id.isdigit():
            return {"mode": "iframe", "embed_url": f"https://player.vimeo.com/video/{video_id}"}

    return {"mode": "video", "embed_url": None}


