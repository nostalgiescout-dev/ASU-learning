    if not lesson:
        flash("Lesson not found.", "danger")
        return redirect(url_for("courses.detail", course_id=course_id))

    lesson_count = (fetchone(
        "SELECT COUNT(*) as c FROM videos WHERE course_id=? AND video_type='lesson'",
        (course_id,),
    ) or {}).get("c", 0)
    existing_quizzes = _fetch_lesson_quiz_questions(video_id, lesson)
    form_data = (
        _lesson_form_data_from_request(request.form, lesson.get("order_index", 1))
        if request.method == "POST"
        else _lesson_form_data_from_lesson(lesson, existing_quizzes)
    )

    if request.method == "POST":
        video_source = request.form.get("video_source", "link")
        title = request.form.get("title", "").strip()
        description = request.form.get("description", "").strip()
        video_url = request.form.get("video_url", "").strip()
        video_file = request.files.get("video_file")
        thumbnail_url = request.form.get("thumbnail_url", "").strip()
        duration_secs = request.form.get("duration_secs", 0, type=int)
        order_index = request.form.get("order_index", 0, type=int)
        xp_reward = request.form.get("xp_reward", 50, type=int)
        is_published = 1 if request.form.get("is_published") else 0
        quizzes = _quiz_payloads_from_form(request.form)

        errors = []
        if not title:
            errors.append("Lesson title is required.")
        if video_source not in {"link", "upload"}:
            errors.append("Choose a valid video source.")
        if video_source == "link" and not video_url:
            errors.append("Video URL is required.")
        if video_source == "upload" and video_file and video_file.filename and not _allowed_lesson_video(video_file.filename):
            errors.append("Please upload a valid video file (.mp4, .mov, .webm, .m4v).")
        if duration_secs < 0:
            errors.append("Duration must be positive.")
        if order_index < 0:
            errors.append("Order index must be positive.")
        errors.extend(_validate_quiz_payloads(quizzes))

        if errors:
            for error in errors:
                flash(error, "danger")
            return render_template(
                "courses/add_lesson.html",
                course=course,
                lesson=lesson,
                form_data=form_data,
                lesson_count=lesson_count,
                next_order=lesson.get("order_index", 1),
                is_edit=True,
            )

        final_video_url = lesson["video_url"]
        if video_source == "link":
            final_video_url = video_url
        elif video_file and video_file.filename:
            try:
                final_video_url = _save_lesson_video(video_file)
            except ValueError as exc:
                flash(str(exc), "danger")
                return render_template(
                    "courses/add_lesson.html",
                    course=course,
                    lesson=lesson,
                    form_data=form_data,
                    lesson_count=lesson_count,
                    next_order=lesson.get("order_index", 1),
                    is_edit=True,
                )

        primary_quiz = _primary_quiz_payload(quizzes)
        with db_session() as conn:
            conn.execute(
                "UPDATE videos SET title=?, description=?, video_url=?, thumbnail_url=?, duration_secs=?, order_index=?, xp_reward=?, is_published=?, quiz_question=?, quiz_option_a=?, quiz_option_b=?, quiz_option_c=?, quiz_option_d=?, quiz_correct_option=?, quiz_explanation=? WHERE id=? AND course_id=?",
                (
                    title,
                    description,
                    final_video_url,
                    thumbnail_url,
                    duration_secs,
                    order_index,
                    xp_reward,
                    is_published,
                    primary_quiz["quiz_question"] or None,
                    primary_quiz["quiz_option_a"] or None,
                    primary_quiz["quiz_option_b"] or None,
                    primary_quiz["quiz_option_c"] or None,
                    primary_quiz["quiz_option_d"] or None,
                    primary_quiz["quiz_correct_option"] or None,
                    primary_quiz["quiz_explanation"] or None,
                    video_id,
                    course_id,
                ),
            )
            _replace_lesson_quiz_questions(conn, video_id, quizzes)
        if is_published and not lesson.get("is_published") and course.get("is_published"):
            _notify_enrolled_users_about_lesson(course_id, video_id, title, user_id)

        if lesson.get("video_url") and lesson["video_url"] != final_video_url:
            _delete_local_video_file(lesson["video_url"])

        flash("Lesson updated successfully.", "success")
        return redirect(url_for("courses.detail", course_id=course_id))

    return render_template(
        "courses/add_lesson.html",
        course=course,
        lesson=lesson,
        form_data=form_data,
        lesson_count=lesson_count,
        next_order=lesson.get("order_index", 1),
        is_edit=True,
    )


