def _allowed_lesson_video(filename: str) -> bool:
    return "." in filename and filename.rsplit(".", 1)[1].lower() in ALLOWED_LESSON_VIDEO_EXTENSIONS


def _save_lesson_video(file_storage) -> str:
    filename = secure_filename(file_storage.filename or "")
    if not filename or not _allowed_lesson_video(filename):
        raise ValueError("Please upload a valid video file (.mp4, .mov, .webm, .m4v).")

    uploads_dir = Path(current_app.static_folder) / "uploads" / "content" / "lesson"
    uploads_dir.mkdir(parents=True, exist_ok=True)
    target = uploads_dir / filename

    if target.exists():
        stem = target.stem
        suffix = target.suffix
        counter = 1
        while target.exists():
            target = uploads_dir / f"{stem}_{counter}{suffix}"
            counter += 1

    file_storage.save(target)
    relative_path = target.relative_to(Path(current_app.static_folder)).as_posix()
    return url_for("static", filename=relative_path)


def _delete_local_video_file(video_url: str) -> None:
    static_prefix = url_for("static", filename="")
    if not video_url.startswith(static_prefix):
        return

    relative_path = video_url.replace(static_prefix, "", 1).lstrip("/")
    disk_path = Path(current_app.static_folder) / relative_path
    try:
        if disk_path.exists():
            disk_path.unlink()
    except OSError:
        pass


def _fetch_course_categories(with_counts: bool = False) -> list[dict]:
    if with_counts:
        return fetchall(
            """
            SELECT
                cc.*,
                COUNT(c.id) AS course_count
            FROM course_categories cc
            LEFT JOIN courses c ON c.category = cc.slug
            GROUP BY cc.id
            ORDER BY CASE WHEN cc.slug='general' THEN 1 ELSE 0 END, cc.name COLLATE NOCASE
            """
        )

