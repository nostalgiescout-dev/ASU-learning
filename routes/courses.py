def _quiz_payloads_from_form(form, include_blank: bool = False) -> list[dict]:
    fields = {
        "quiz_question": form.getlist("quiz_question[]"),
        "quiz_option_a": form.getlist("quiz_option_a[]"),
        "quiz_option_b": form.getlist("quiz_option_b[]"),
        "quiz_option_c": form.getlist("quiz_option_c[]"),
        "quiz_option_d": form.getlist("quiz_option_d[]"),
        "quiz_correct_option": form.getlist("quiz_correct_option[]"),
        "quiz_explanation": form.getlist("quiz_explanation[]"),
    }
    total = max((len(values) for values in fields.values()), default=0)
    quizzes: list[dict] = []

    for index in range(total):
        quiz = {
            key: (values[index].strip() if index < len(values) else "")
            for key, values in fields.items()
        }
        quiz["quiz_correct_option"] = quiz["quiz_correct_option"].lower()
        if include_blank or any(quiz.values()):
            quizzes.append(quiz)

    return quizzes


def _validate_quiz_payload(quiz: dict) -> list[str]:
    if not any(quiz.values()):
        return []

    errors = []
    if not quiz["quiz_question"]:
        errors.append("Quiz question is required when adding a quiz.")
    if not quiz["quiz_option_a"] or not quiz["quiz_option_b"]:
        errors.append("Quiz options A and B are required when adding a quiz.")

    available_options = {
        key for key, text in {
            "a": quiz["quiz_option_a"],
            "b": quiz["quiz_option_b"],
            "c": quiz["quiz_option_c"],
            "d": quiz["quiz_option_d"],
        }.items() if text
    }
    if quiz["quiz_correct_option"] not in available_options:
        errors.append("Select a correct answer from the filled quiz options.")

    return errors


def _validate_quiz_payloads(quizzes: list[dict]) -> list[str]:
    errors: list[str] = []
    for index, quiz in enumerate(quizzes, start=1):
        for error in _validate_quiz_payload(quiz):
            errors.append(f"Quiz question {index}: {error}")
    return errors


