def _format_completion_date(timestamp: str | None, language: str = "en") -> str:
    if not timestamp:
        date_value = datetime.utcnow().date()
    else:
        try:
            date_value = datetime.strptime(timestamp, "%Y-%m-%d %H:%M:%S").date()
        except ValueError:
            try:
                date_value = datetime.strptime(timestamp[:10], "%Y-%m-%d").date()
            except ValueError:
                return timestamp[:10]
    locale = language if language in COURSE_CERTIFICATE_LANGUAGES else "en"
    if locale == "ar":
        return f"{date_value.day} {ARABIC_MONTH_NAMES[date_value.month]} {date_value.year}"
    try:
        return format_date(date_value, format="long", locale=locale)
    except Exception:
        return date_value.strftime("%B %d, %Y")


def _contains_arabic(text: str | None) -> bool:
    return bool(text and re.search(r"[\u0600-\u06FF]", text))


def _looks_french(text: str | None) -> bool:
    if not text:
        return False
    lowered = text.lower()
    if re.search(r"[àâçéèêëîïôûùüÿœæ]", lowered):
        return True
    padded = f" {lowered} "
    return any(marker in padded for marker in (" le ", " la ", " les ", " des ", " de ", " du ", " pour ", " et "))


def _resolve_course_language(course: dict | None) -> str:
    if not course:
        return "en"
    language = (course.get("language") or "").strip().lower()
    if language in COURSE_CERTIFICATE_LANGUAGES:
        return language
    title = course.get("title", "")
    if _contains_arabic(title):
        return "ar"
    if _looks_french(title):
        return "fr"
    return "en"


def _resolve_course_title_for_certificate(course: dict, language: str) -> str:
    if language == "ar":
        if _contains_arabic(course.get("title")):
            return course.get("title") or ""
        if course.get("title_ar"):
            return course.get("title_ar") or course.get("title") or ""
    return course.get("title") or course.get("title_ar") or ""


def _prepare_certificate_text(text: str, language: str) -> str:
    value = (text or "").strip()
    if language == "ar" and value and arabic_reshaper and get_display:
        return get_display(arabic_reshaper.reshape(value))
    return value


