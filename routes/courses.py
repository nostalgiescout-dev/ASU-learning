@courses_bp.route("/<int:course_id>/lesson/<int:video_id>", methods=["GET", "POST"])
@login_required
def lesson_detail(course_id, video_id):
    uid = session["user_id"]
    notification_service.mark_related_as_read(uid, entity_type="course", entity_id=course_id)
    notification_service.mark_related_as_read(uid, entity_type="lesson", entity_id=video_id)
    course = fetchone(
        "SELECT c.*, cc.name as category_name, cc.emoji as category_emoji, u.full_name, u.avatar_url as uavatar, u.level_rank "
        "FROM courses c JOIN users u ON c.instructor_id=u.id "
        "LEFT JOIN course_categories cc ON cc.slug = c.category "
        "WHERE c.id=?",
        (course_id,),
    )
    if not course:
        flash("Course not found.", "danger")
        return redirect(url_for("courses.index"))

    role = _current_user_role()
    can_manage = _can_manage_course(course, uid, role)
    enrollment = fetchone("SELECT * FROM enrollments WHERE user_id=? AND course_id=?", (uid, course_id))
    if not enrollment and not can_manage:
        flash("Enroll in the course to access this lesson.", "warning")
        return redirect(url_for("courses.detail", course_id=course_id))

    lessons = fetchall(
        "SELECT * FROM videos WHERE course_id=? AND video_type='lesson' "
        + ("" if can_manage else "AND is_published=1 ")
        + "ORDER BY order_index ASC",
        (course_id,),
    )
    lesson = next((item for item in lessons if item["id"] == video_id), None)
    if not lesson:
        flash("Lesson not found.", "danger")
        return redirect(url_for("courses.detail", course_id=course_id))

    completed_rows = fetchall("SELECT video_id FROM lesson_completions WHERE user_id=? AND course_id=?", (uid, course_id))
    completed_ids = {row["video_id"] for row in completed_rows}
    lesson_completed = video_id in completed_ids

    def dur_fmt(seconds):
        if not seconds:
            return "-"
        minutes, sec = divmod(int(seconds), 60)
        return f"{minutes}:{sec:02d}"

    for index, item in enumerate(lessons, start=1):
        item["duration_display"] = dur_fmt(item.get("duration_secs"))
        item["position"] = index
        item["is_completed"] = item["id"] in completed_ids

    lesson["duration_display"] = dur_fmt(lesson.get("duration_secs"))
    lesson.update(_lesson_player_meta(lesson.get("video_url")))
    lesson_index = next((idx for idx, item in enumerate(lessons) if item["id"] == video_id), 0)
    previous_lesson = lessons[lesson_index - 1] if lesson_index > 0 else None
    next_lesson = lessons[lesson_index + 1] if lesson_index + 1 < len(lessons) else None

    quiz_questions = _fetch_lesson_quiz_questions(video_id, lesson)
    for index, question in enumerate(quiz_questions, start=1):
        question["position"] = index
        question["field_name"] = f"quiz_answer_{question['id'] or index}"
        question["options"] = [
            (key, text) for key, text in [
                ("a", question.get("quiz_option_a")),
                ("b", question.get("quiz_option_b")),
                ("c", question.get("quiz_option_c")),
                ("d", question.get("quiz_option_d")),
            ] if text
        ]

    has_quiz = bool(quiz_questions)
    selected_answers: dict[str, str] = {}
    quiz_feedback = None
    quiz_feedback_type = "success"

    if request.method == "POST":
        action = request.form.get("action", "complete")
        if action == "submit_quiz" and has_quiz:
            answer_payload: dict[str, str] = {}
            missing_answer = False
            has_wrong_answer = False
            correct_answers = 0

            for question in quiz_questions:
                selected_answer = request.form.get(question["field_name"], "").strip().lower()
                selected_answers[question["field_name"]] = selected_answer
                answer_payload[str(question["id"] or question["position"])] = selected_answer
                valid_options = {key for key, _ in question["options"]}
                if selected_answer not in valid_options:
                    missing_answer = True
                    continue
                if selected_answer != question.get("quiz_correct_option"):
                    has_wrong_answer = True
                    continue
                correct_answers += 1

            if missing_answer:
                quiz_feedback = "Answer every quiz question before submitting."
                quiz_feedback_type = "danger"
            elif has_wrong_answer:
                quiz_feedback = "One or more answers are not correct yet. Try again."
                quiz_feedback_type = "danger"
            else:
                result = _mark_lesson_complete(
                    uid,
                    course_id,
                    video_id,
                    json.dumps(answer_payload, sort_keys=True),
                    correct_answers / max(1, len(quiz_questions)),
                )
                if result.get("reason") == "not_enrolled":
                    flash("Enroll in the course to track progress.", "warning")
                    return redirect(url_for("courses.detail", course_id=course_id))
                lesson_completed = True
                enrollment = fetchone("SELECT * FROM enrollments WHERE user_id=? AND course_id=?", (uid, course_id))
                quiz_feedback = "All answers are correct. Lesson completed."
                if result.get("already_completed"):
                    quiz_feedback = "All answers are correct. This lesson was already completed."
                elif result.get("course_completed"):
                    quiz_feedback = "All answers are correct. Lesson completed and the whole course is now complete."
                quiz_feedback_type = "success"
        elif action == "complete":
            result = _mark_lesson_complete(uid, course_id, video_id)
            if result.get("reason") == "not_enrolled":
                flash("Enroll in the course to track progress.", "warning")
                return redirect(url_for("courses.detail", course_id=course_id))
            lesson_completed = True
            enrollment = fetchone("SELECT * FROM enrollments WHERE user_id=? AND course_id=?", (uid, course_id))
            flash("Lesson completed.", "success" if not result.get("already_completed") else "info")
            return redirect(url_for("courses.lesson_detail", course_id=course_id, video_id=video_id))

    return render_template(
        "courses/lesson.html",
        course=course,
        lesson=lesson,
        lessons=lessons,
        enrollment=enrollment,
        can_manage=can_manage,
        lesson_completed=lesson_completed,
        has_quiz=has_quiz,
        quiz_questions=quiz_questions,
        selected_answers=selected_answers,
        quiz_feedback=quiz_feedback,
        quiz_feedback_type=quiz_feedback_type,
        previous_lesson=previous_lesson,
        next_lesson=next_lesson,
    )


