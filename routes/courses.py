def _slugify_course_category(value: str) -> str:
    slug = re.sub(r"[^a-z0-9]+", "-", value.strip().lower())
    return slug.strip("-") or "general"


def _build_unique_course_category_slug(name: str, current_id: int | None = None) -> str:
    base_slug = _slugify_course_category(name)
    slug = base_slug
    suffix = 2

    while True:
        existing = fetchone("SELECT id FROM course_categories WHERE slug=?", (slug,))
        if not existing or existing["id"] == current_id:
            return slug
        slug = f"{base_slug}-{suffix}"
        suffix += 1


